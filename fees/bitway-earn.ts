import { SimpleAdapter, FetchOptions } from "../adapters/types";
import { CHAIN } from "../helpers/chains";
import fetchURL from "../utils/fetchURL";

const fetch: any = async (options: FetchOptions) => {
  const data = await fetchURL(
    `https://api.bitway.com/bitway-earn-api/defillama/fees-revenue?startTimestamp=${options.startTimestamp}&endTimestamp=${options.endTimestamp}`
  );

  if (data?.fees == undefined || data?.revenue == undefined) {
    throw new Error(`No data found for date ${options.dateString}`);
  }

  const fees = Number(data.fees);
  const revenue = Number(data.revenue);
  const supplySideRevenue = fees - revenue;

  return {
    dailyFees: fees,
    dailyRevenue: revenue,
    dailyProtocolRevenue: revenue,
    dailySupplySideRevenue: supplySideRevenue,
  };
};

const methodology = {
  Fees: "Total fees, including yield generated by vault strategies and flash unstake penalties.",
  Revenue: "Total revenue earned by the protocol, excluding yield distributed to depositors.",
  ProtocolRevenue: "Total revenue earned by the protocol, excluding yield distributed to depositors.",
  SupplySideRevenue: "Yield distributed to depositors from vault strategies.",
};

const adapter: SimpleAdapter = {
  version: 1,
  fetch,
  chains: [CHAIN.BSC],
  start: "2026-01-01",
  methodology,
  allowNegativeValue: true,
};

export default adapter;
