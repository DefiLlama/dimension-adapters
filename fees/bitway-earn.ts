import { SimpleAdapter, FetchOptions } from "../adapters/types";
import { CHAIN } from "../helpers/chains";
import { METRIC } from "../helpers/metrics";
import ADDRESSES from "../helpers/coreAssets.json";

// https://docs.bitway.com/bitway-earn/detradfi
const STRATEGIES = [
    {
        // ABSOLUTE_RETURN
        vault: "0x5C4a6903732532eeB3AE0803e062d8AE25d52BD1",
        tokens: [
            ADDRESSES.bsc.USDT,
            ADDRESSES.bsc.USDC,
            "0xcE24439F2D9C6a2289F741120FE202248B666666", // U
            "0x444045B0EE1ee319A660a5E3d604CA0ffA35ACaA", // BTW
        ],
    },
    {
        // CORE_ALPHA
        vault: "0xb82E32062C773c7748776C06FdB11B92EDAE3B63",
        tokens: [
            ADDRESSES.bsc.USDT,
            ADDRESSES.bsc.USD1,
            "0xcE24439F2D9C6a2289F741120FE202248B666666", // U
        ],
    },
];

const abis = {
    totalSupply: "uint256:totalSupply",
    supportedTokenToLPToken: "function supportedTokenToLPToken(address) view returns (address)",
    convertToAssets: "function convertToAssets(uint256, address) view returns (uint256)",
    flashWithdraw: "event FlashWithdraw(address indexed _user, address indexed _token, uint256 indexed _amount, uint256 _principalAmount, uint256 _rewardAmount, uint256 _fee)",
};

const fetch = async ({ createBalances, getLogs, fromApi, toApi, api }: FetchOptions) => {
    const dailyFees = createBalances();
    const dailySupplySideRevenue = createBalances();
    const dailyProtocolRevenue = createBalances();

    const targets = STRATEGIES.map((s) => s.vault);

    const ONE = String(1e18);

    const allLpTokenCalls = STRATEGIES.flatMap(({ vault, tokens }) =>
        tokens.map((token) => ({ target: vault, params: [token] }))
    );
    const allLpTokens: string[] = await api.multiCall({
        abi: abis.supportedTokenToLPToken,
        calls: allLpTokenCalls,
        permitFailure: true,
    });

    const allTotalSupply = await api.multiCall({ calls: allLpTokens, abi: abis.totalSupply, permitFailure: true });

    const convertCalls = STRATEGIES.flatMap(({ vault, tokens }) =>
        tokens.map((token) => ({ target: vault, params: [ONE, token] }))
    );
    const allTokens = STRATEGIES.flatMap(({ tokens }) => tokens);

    const [sharePricesStart, sharePricesEnd] = await Promise.all([
        fromApi.multiCall({ abi: abis.convertToAssets, calls: convertCalls, permitFailure: true }),
        toApi.multiCall({ abi: abis.convertToAssets, calls: convertCalls, permitFailure: true }),
    ]);

    for (let i = 0; i < allTokens.length; i++) {
        const totalSupply = allTotalSupply[i];
        const priceStart = sharePricesStart[i];
        const priceEnd = sharePricesEnd[i];
        if (!totalSupply || !priceStart || !priceEnd) continue;

        const priceGrowth = Number(priceEnd) - Number(priceStart);

        const totalYield = (priceGrowth * Number(totalSupply)) / 1e18;
        dailyFees.add(allTokens[i], totalYield, METRIC.ASSETS_YIELDS);
        dailySupplySideRevenue.add(allTokens[i], totalYield, METRIC.ASSETS_YIELDS);

    }

    // Track protocol revenue from flash withdraw penalty fees
    const flashWithdrawLogs = await getLogs({
        targets,
        eventAbi: abis.flashWithdraw,
    });

    flashWithdrawLogs.forEach((e: any) => {
        dailyFees.add(e._token, e._fee, METRIC.DEPOSIT_WITHDRAW_FEES);
        dailyProtocolRevenue.add(e._token, e._fee, METRIC.DEPOSIT_WITHDRAW_FEES);
    });

    return {
        dailyFees,
        dailyRevenue: dailyProtocolRevenue,
        dailyProtocolRevenue,
        dailySupplySideRevenue,
    };
};

const methodology = {
    Fees: "Total fees including yield generated by vault strategies and flash unstake penalty fees.",
    Revenue: "Total revenue including yield distributed to depositors and flash unstake penalty fees.",
    ProtocolRevenue: "Flash unstake penalty fees retained by the Bitway protocol.",
    SupplySideRevenue: "Yield generated by Bitway Earn vault strategies and distributed to depositors.",
};

const breakdownMethodology = {
    Fees: {
        [METRIC.ASSETS_YIELDS]: "Yield generated by vault strategies for depositors.",
        [METRIC.DEPOSIT_WITHDRAW_FEES]: "Flash unstake penalty fees charged on early withdrawals.",
    },
    Revenue: {
        [METRIC.ASSETS_YIELDS]: "Yield generated by vault strategies for depositors.",
        [METRIC.DEPOSIT_WITHDRAW_FEES]: "Flash unstake penalty fees collected by the protocol.",
    },
    ProtocolRevenue: {
        [METRIC.DEPOSIT_WITHDRAW_FEES]: "Flash unstake penalty fees retained by the Bitway protocol.",
    },
    SupplySideRevenue: {
        [METRIC.ASSETS_YIELDS]: "Yield distributed to vault depositors.",
    },
};

const adapter: SimpleAdapter = {
    version: 2,
    pullHourly: true,
    fetch,
    chains: [CHAIN.BSC],
    start: "2025-10-18",
    methodology,
    breakdownMethodology,
    allowNegativeValue: true,
};

export default adapter;
