const methodology = {
  Fees: "Arbitration fees paid by dispute creators distributed to jurors, plus net appeal contributions.",
  Revenue: "All arbitration fees generated by the protocol.",
  SupplySideRevenue: "Fees distributed as rewards to coherent jurors who vote correctly.",
};

const adapter: SimpleAdapter = {
  version: 2,
  adapter: {
    [CHAIN.ARBITRUM]: {
      fetch: fetchArbitrum,
      start: "2024-02-01",
    },
    [CHAIN.ETHEREUM]: {
      fetch: fetchEthereum,
      start: "2018-11-01",
    },
    [CHAIN.XDAI]: {
      fetch: fetchGnosis,
      start: "2020-03-01",
    },
  },
  methodology,
};

export default adapter;
