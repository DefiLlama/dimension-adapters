import { FetchOptions, SimpleAdapter } from "../adapters/types";
import { CHAIN } from "../helpers/chains";
import coreAssets from "../helpers/coreAssets.json";

const USDC = coreAssets.hyperliquid.USDC;

const MONETRIX_VAULT = '0x5586c2c8223C73Ec0B41D6352748e6c173372E11';
const INSTANT_REDEEM = '0xF4F03Bbfb6D314fD1D1E22012905d778f5B0adD2';
const INSTANT_UNSTAKE = '0x7592b9Fc42E000783d491739c77CcB86F19c4132';

const YIELD_DISTRIBUTED_ABI = 'event YieldDistributed(uint256 totalYield, uint256 userShare, uint256 insuranceShare, uint256 foundationShare)';
const INSTANT_EXITED_ABI = 'event InstantExited(address indexed user, uint256 inputAmount, uint256 payout, uint256 fee)';

const fetch = async (options: FetchOptions) => {
  const dailyFees = options.createBalances();
  const dailyRevenue = options.createBalances();
  const dailySupplySideRevenue = options.createBalances();
  const dailyUserFees = options.createBalances();

  // Yield generated by the protocol's delta-neutral positions on Hyperliquid Core,
  // split on distribution: userShare -> sUSDM stakers, insuranceShare -> insurance
  // fund, foundationShare -> foundation
  const yieldLogs = await options.getLogs({ target: MONETRIX_VAULT, eventAbi: YIELD_DISTRIBUTED_ABI });
  yieldLogs.forEach((log: any) => {
    dailyFees.add(USDC, log.totalYield);
    dailySupplySideRevenue.add(USDC, log.userShare);
    dailyRevenue.add(USDC, log.insuranceShare);
    dailyRevenue.add(USDC, log.foundationShare);
  });

  // Instant exit fees paid by users to the fee recipient. InstantRedeem fees are
  // in USDC, InstantUnstake fees in USDM — both 6 decimals and $1-pegged, booked as USDC
  const exitLogs = await options.getLogs({ targets: [INSTANT_REDEEM, INSTANT_UNSTAKE], eventAbi: INSTANT_EXITED_ABI });
  exitLogs.forEach((log: any) => {
    dailyFees.add(USDC, log.fee);
    dailyRevenue.add(USDC, log.fee);
    dailyUserFees.add(USDC, log.fee);
  });

  return { dailyFees, dailyRevenue, dailySupplySideRevenue, dailyUserFees };
};

const adapter: SimpleAdapter = {
  version: 2,
  chains: [CHAIN.HYPERLIQUID],
  fetch,
  start: '2026-06-10',
  methodology: {
    Fees: "Yield generated by the protocol's delta-neutral positions on Hyperliquid Core (funding payments, HLP and borrow-lend yield), measured at distribution via the vault's YieldDistributed event, plus instant redeem/unstake fees paid by users.",
    Revenue: "Share of distributed yield routed to the insurance fund and the foundation, plus instant redeem/unstake fees.",
    SupplySideRevenue: "Share of distributed yield paid to sUSDM stakers.",
    UserFees: "Instant redeem/unstake fees paid by users for skipping the delayed exit flow.",
  },
};

export default adapter;
